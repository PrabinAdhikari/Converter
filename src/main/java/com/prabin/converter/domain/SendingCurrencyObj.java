package com.prabin.converter.domain;

public class SendingCurrencyObj {
	String from;
	String to;
	double amtFrom;
	double amtTo;
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getTo() {
		return to;
	}
	public void setTo(String to) {
		this.to = to;
	}
	public double getAmtFrom() {
		return amtFrom;
	}
	public void setAmtFrom(double amtFrom) {
		this.amtFrom = amtFrom;
	}
	public double getAmtTo() {
		return amtTo;
	}
	public void setAmtTo(double amtTo) {
		this.amtTo = amtTo;
	}
}
